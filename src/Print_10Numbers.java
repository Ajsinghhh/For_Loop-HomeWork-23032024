/* Print any 10 numbers between given/user input range. For eg. Print numbers
between 23-33 */


// Class NAme
public class Print_10Numbers {

    //Main Method
    public static void main(String[] args) {

//initialization and declaration
        double x = 50;

// For Loop
        for (x = 50; x <= 60; x++) {
            System.out.println(x);
        }

    }

}
